package Model;

public class Cliente {

	String nombre;
	String contacto;
	String fecha_nacimiento;
	String nacionalidad;
	
	public Cliente(String nombre, String contacto, String fecha_nacimiento, String nacionalidad) {
		this.nombre = nombre;
		this.contacto = contacto;
		this.fecha_nacimiento = fecha_nacimiento;
		this.nacionalidad = nacionalidad;
		
	}

}
